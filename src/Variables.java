public class Variables {
    public static void main(String [] args){
        int myInt = 557; // 32 бита
        short myShort = 3000; // от -32000 до 32000 - 16 битов
        long myLong = 320000;
        byte myByte = 120; // от -128 до 127 - 8 битов

        double myDouble = 4234234.2342; // 64 бита
        float myFloat = 433423.23f; // 32 бита

        char myChar = 'п'; // символы из одной буквы, например
        boolean myBoolean = false; // ещё true
        System.out.println(myBoolean);
    }
}
